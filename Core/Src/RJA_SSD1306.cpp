void OLED::fillCircleHelper(int x0, int y0, int r, int cornername, int delta, bool colour)	//was static
{
	int f     = 1 - r;
	int ddF_x = 1;
	int ddF_y = -2 * r;
	int x     = 0;
	int y     = r;

	while (x < y)
	{
		if (f >= 0)
		{
			y--;
			ddF_y += 2;
			f += ddF_y;
		}
		x++;
		ddF_x += 2;
		f += ddF_x;

		if (cornername & 0x1)
		{
			fastVLine(x0+x, y0-y, 2*y+1+delta, colour);
			fastVLine(x0+y, y0-x, 2*x+1+delta, colour);
		}
		if (cornername & 0x2)
		{
		  fastVLine(x0-x, y0-y, 2*y+1+delta, colour);
		  fastVLine(x0-y, y0-x, 2*x+1+delta, colour);
		}
	}
}

